package org.example;

public class Person {
    private int age;
    private String name;
    private String work;

    public int getAge(){
        return this.age;
    }
    public String getName(){
        return this.name;
    }
    public String getWork(){
        return this.work;
    }
    public void setAge(int age){
        this.age = age;
    }
    public void setName(String name){
        this.name = name;
    }
    public void setWork(String work){
        this.work = work;
    }
}
